<h3>What can I do if I'm personally affected by the housing crisis?</h3>
<p>If you're struggling with housing affordability, there are several organizations and resources in Auckland that offer support. Reach out to local community groups, government agencies, or housing advocacy organizations for assistance and guidance on available options.</p>
</div>

<div class="FAQques">
<h3>What can individuals do to help?</h3>
