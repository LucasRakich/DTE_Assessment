<div class="FAQques">
<h3>Who is most affected by the housing crisis in Auckland?</h3>
<p>While the crisis affects everyone, it disproportionately impacts families, young people, and low-income workers. Many of these individuals are either being pushed out of the housing market or are forced into overcrowded living conditions.</p>
</div>

<div class="FAQques">
<h3>What role does Rosmini College play in addressing the housing crisis?</h3>
<p>Rosmini College is actively involved in addressing Auckland's affordable housing crisis by fostering a community-driven approach. The college advocates for local government policy changes and supports outreach programs that aim to amplify the voices of those affected by the housing shortage.</p>
</div>

